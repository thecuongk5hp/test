/*
    Phân biệt giữa abstract method và method, khi nào thì sử dụng và cho ví dụ minh họa.
*/
/*
    Abstract method
    Định nghĩa: Phương thức trừu tượng là một phương thức không có triển khai (implementation) trong lớp chứa nó.
    Nó chỉ được khai báo trong lớp trừu tượng và yêu cầu các lớp con kế thừa từ lớp trừu tượng đó phải triển khai (override) phương thức này.
    Sử dụng: Phương thức trừu tượng được sử dụng khi bạn muốn định nghĩa một hành vi chung cho các lớp con,
    nhưng không thể cung cấp một triển khai chung cho hành vi đó trong lớp trừu tượng.
*/
/*
    method
    Định nghĩa: Phương thức là một hành vi cụ thể của một đối tượng hoặc một lớp.
    Nó được triển khai bên trong lớp và có một thân phương thức (implementation) cụ thể.
    Sử dụng: Phương thức được sử dụng để thực hiện các hành động cụ thể của một đối tượng hoặc một lớp.
*/ 
